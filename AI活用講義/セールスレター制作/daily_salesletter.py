def get_past_data():
    past = []
    for folder in os.listdir(BASE_DIR):
        folder_path = os.path.join(BASE_DIR, folder)
        if os.path.isdir(folder_path):
            for f in os.listdir(folder_path):
                if f.endswith(".md"):
                    with open(os.path.join(folder_path, f), "r") as fp:
                        past.append(fp.read()[:1000])
    return "\n\n---\n\n".join(past[:10]) if past else ""

def run_daily_task():
    today_str = date.today().strftime("%Y-%m-%d")
    today_display = date.today().strftime("%Y年%m月%d日")

    if today_str not in SCHEDULE:
        print(f"本日（{today_display}）のスケジュールはありません")
        return

    folder, task_name, task_theme = SCHEDULE[today_str]
    past_data = get_past_data()

    prompt = f"""
{QUEST_CONTEXT}

【本日のタスク】
日付：{today_display}
フォルダ：{folder}
タスク名：{task_name}
テーマ：{task_theme}

【過去の蓄積データ（参照用）】
{past_data}

【調査・参照してほしい情報】
1. 日本国内のAI活用・AI副業・AI自動化系の競合セールスレター・LP
2. 米国のAI活用講座・エージェントAI系の最新セールス表現
3. 40代サラリーマン・経営者向けのオンラインスクールの訴求方法

【出力形式】
# {task_name}

## テーマ：{task_theme}

## ブレインダンプ・深堀り（20〜30項目）
各項目に必ず「WHY（なぜこれが重要か）」を付けること
番号付きリストで出力すること
競合・米国事例から学んだ表現・アイデアも含めること

## セールスレターへの活用メモ
このデータをセールスレターにどう使うか

## 次回への引き継ぎメモ
明日のタスクに向けて特に意識すべきこと
"""

    print(f"本日のタスク：{task_name} を実行中...")

    try:
        response = client.models.generate_content(
            model="gemini-2.5-flash",
            contents=prompt,
            config=types.GenerateContentConfig(
                tools=[types.Tool(google_search=types.GoogleSearch())]
            )
        )
    except Exception:
        response = client.models.generate_content(
            model="gemini-2.5-flash",
            contents=prompt
        )

    save_folder = os.path.join(BASE_DIR, folder)
    os.makedirs(save_folder, exist_ok=True)
    output_file = os.path.join(
        save_folder,
        f"{today_str}_{task_name}.md"
    )

    with open(output_file, "w") as f:
        f.write(response.text)

    log_file = os.path.join(
        BASE_DIR, "毎日の作業ログ",
        f"{today_str}_作業ログ.md"
    )
    os.makedirs(os.path.dirname(log_file), exist_ok=True)
    with open(log_file, "w") as f:
        f.write(f"# 作業ログ {today_display}\n\n")
        f.write(f"- タスク：{task_name}\n")
        f.write(f"- テーマ：{task_theme}\n")
        f.write(f"- 保存先：{output_file}\n")
        f.write(f"- 完了時刻：{datetime.now().strftime('%H:%M')}\n")

    print(f"✅ 完了：{output_file}")
    print(f"✅ ログ保存：{log_file}")

if __name__ == "__main__":
    run_daily_task()
